"""Modudlo que contiene diferentes calculos matematicos"""
def Suma(n1,n2):
    """
    La funsion suma resuleve una suma entre dos numeros pasados por parametros
    """
    print("Resultado: ", n1+n2)

def Resta(n1,n2):
    """
    La funsion resta resuleve una testa entre dos numeros pasados por parametros
    """
    print("Resultado: ",n1-n2)

def Multiplicar(n1,n2):
    """
    La funsion multiplicar resuleve una multipicacion entre dos numeros pasados por parametros
    """
    print("Resultado: ",n1*n2)